    private void jTextField_empIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_empIDKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
            model.setRowCount(0);
            if(jTextField_empID.getText().trim().length()==0||jTextField_empID.getText()==null){
                JOptionPane.showMessageDialog(this, "Please Enter Valid Date!");
            }
            else{
                
                try{
                    int empID=Integer.parseInt(jTextField_empID.getText());
                    empSalaryList=db_crud.readEmployeeSalaryDB(empID);
                    List<EmployeeInformation> employeeInformations=db_crud.readAllDB();
                    if(empSalaryList.isEmpty()){
                        JOptionPane.showMessageDialog(this, "Salary for Employee ID: "+empID+" Not Found!");
                    }
                    else if(employeeInformations.isEmpty()){
                        JOptionPane.showMessageDialog(this, "Employees' Informations Not Found! ");
                    }
                    else{
                       // DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
                        Object rowData[]=new Object[15];
                        for(EmployeeSalary e:empSalaryList){
                            int emp_index=searchList(employeeInformations,0,employeeInformations.size(), e.getEmployeeId());
                            if(emp_index!=-1){ // if employee whose salary generated is found
                                rowData[0]=e.getEmployeeId();
                                rowData[1]=employeeInformations.get(emp_index).getEmployeeName();
                                rowData[2]=e.getSalaryDate();
                                rowData[3]=employeeInformations.get(emp_index).getGrossSalary();
                                rowData[4]=e.getWorkTime();
                                rowData[5]=e.getEarnedSalary();
                                rowData[6]=employeeInformations.get(emp_index).getPFund();
                                rowData[7]=employeeInformations.get(emp_index).getItax();
                                rowData[8]=employeeInformations.get(emp_index).getAdvSalary();
                                rowData[9]=employeeInformations.get(emp_index).getPFLoan();
                                rowData[10]=employeeInformations.get(emp_index).getPersonalLoan();
                                rowData[11]=employeeInformations.get(emp_index).getMessDed();
                                rowData[12]=employeeInformations.get(emp_index).getEOBI();
                                rowData[13]=employeeInformations.get(emp_index).getOtherDed();
                                rowData[14]=e.getNetSalary();
                                model.addRow(rowData);
                                
                            }
                        }
                    }
                }
                catch(NumberFormatException e){
                   JOptionPane.showMessageDialog(this, "Please Enter Valid Employee ID!");
                }
            }
        }
    }//GEN-LAST:event_jTextField_empIDKeyPressed

