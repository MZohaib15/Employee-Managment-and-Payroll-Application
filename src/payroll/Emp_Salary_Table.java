    private void jButton_saveEditedInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_saveEditedInfoActionPerformed
        try{
            int i=jTable1.getSelectedRow();
            int id=(int) model.getValueAt(i,0);
            String date=model.getValueAt(i,2).toString();
            EmployeeInformation emp=db_crud.searchDB(id);
            boolean flag=false;

            int pfund,itax,advSal,pfLoan,PersLoan,MessDed,Eobi,OtherDed;

            try{
                pfund=Integer.parseInt(jTextField_editPfund.getText());
                itax=Integer.parseInt(jTextField_editItax.getText());
                advSal=Integer.parseInt(jTextField_editAdvSal.getText());
                pfLoan=Integer.parseInt(jTextField_editPFloan.getText());
                PersLoan=Integer.parseInt(jTextField_editPersLoan.getText());
                MessDed=Integer.parseInt(jTextField_editMessDed.getText());
                Eobi=Integer.parseInt(jTextField_editEOBI.getText());
                OtherDed=Integer.parseInt(jTextField_editOtherDed.getText());
            }
            catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this, "Error Detected in Deduction Fields! Please recheck your entered values.");
                pfund=0;
                itax=0;
                advSal=0;
                pfLoan=0;
                PersLoan=0;
                MessDed=0;
                Eobi=0;
                OtherDed=0;
                flag=true;
            }

            if(!flag){
                emp.setP_Fund(pfund);
                emp.setItax(itax);
                emp.setAdvSal(advSal);
                emp.setPF_Loan(pfLoan);
                emp.setPersonalLoan(PersLoan);
                emp.setMessDed(MessDed);
                emp.setEOBI(Eobi);
                emp.setOtherDed(OtherDed);
                boolean check=db_crud.updateDB(emp);
                if(check){
                    //JOptionPane.showMessageDialog(this, "Saved Successfully!");
                    EmployeeSalary emp_sal=db_crud.searchEmployeeSalaryDB(id,date);
                    int new_ded_sum=0;
                    new_ded_sum=new_ded_sum+pfund+itax+advSal+pfLoan+PersLoan+MessDed+Eobi+OtherDed;
                    int new_net_salary=emp_sal.getEarnedSalary()-new_ded_sum;
                    emp_sal.setNetSalary(new_net_salary);
                    boolean check2=db_crud.updateEmployeeSalaryDB(emp_sal);
                    if(check2){ // if information updated in both tables of the database successfully
                        JOptionPane.showMessageDialog(this, "Saved Successfully!");
                        model.setValueAt(emp_sal.getNetSalary(),i,14);

                        model.setValueAt(pfund,i,6);
                        model.setValueAt(itax,i,7);
                        model.setValueAt(advSal,i,8);
                        model.setValueAt(pfLoan,i,9);
                        model.setValueAt(PersLoan,i,10);
                        model.setValueAt(MessDed,i,11);
                        model.setValueAt(Eobi,i,12);
                        model.setValueAt(OtherDed,i,13);
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Update Failed!");
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this, "Update Failed!");
                }
            }

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error Occured While Saving! Please recheck your input values.");
        }
    }//GEN-LAST:event_jButton_saveEditedInfoActionPerformed

