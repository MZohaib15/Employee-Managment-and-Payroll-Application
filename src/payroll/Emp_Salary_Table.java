    private void jButton_generateExcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_generateExcelActionPerformed
        // TODO add your handling code here:
        int noOfRows=model.getRowCount();

        int salaryListSize=empSalaryList.size();

        if(noOfRows>0&&salaryListSize<=noOfRows){

            XSSFWorkbook wb=new XSSFWorkbook();
            XSSFSheet ws=wb.createSheet();

            TreeMap<String,Object[]> data=new TreeMap<>();
            int i=-1;
            data.put(Integer.toString(i),new Object[]{"ID","Name","Salary Date","Gross Salary","Total Work Mins","Earned Salary","P-Fund","Itax","Adv.Sal","PF Loan","Personal Loan","Mess Ded","EOBI","Other Ded","Net Salary"});
            i++;
            for(;i<noOfRows;i++){
                try{
                  if(getCellValue(i, 0)!=null){
                    data.put(Integer.toString(i),new Object[]{getCellValue(i, 0),getCellValue(i, 1),getCellValue(i, 2),getCellValue(i, 3),getCellValue(i, 4),getCellValue(i, 5),getCellValue(i, 6),getCellValue(i, 7),getCellValue(i, 8),getCellValue(i, 9),getCellValue(i, 10),getCellValue(i, 11),getCellValue(i, 12),getCellValue(i, 13),getCellValue(i, 14)});
                  }
                }
                catch(NullPointerException ne){
                    System.out.println("Null Pointer Exception");
                }
            }

            Set<String> ids=data.keySet();
            XSSFRow row;
            int rowID=0;

            for(String key: ids){
                row=ws.createRow(rowID++);

                Object[] values=data.get(key);

                int cellID=0;
                for(Object o:values){
                    Cell cell=row.createCell(cellID++);
                    cell.setCellValue(o.toString());
                }
            }

            try{
                FileOutputStream fos=new FileOutputStream(new File("EmployeeSalarySheet.xlsx"));
                wb.write(fos);
                fos.close();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Error Generating Excel File!");
                java.util.logging.Logger.getLogger(Salary_Sheet_Table.class.getName()).log(Level.SEVERE, null, ex);
            }

            JOptionPane.showMessageDialog(this, "Excel File Generated Successfully!");
        }
        else{
            JOptionPane.showMessageDialog(this, "Can Not Generate Empty Excel File!");
        }

    }//GEN-LAST:event_jButton_generateExcelActionPerformed

