    private void jButton_printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_printActionPerformed
        // TODO add your handling code here:
        int count=model.getRowCount();
            if(count<=0){ // if table is not empty
                JOptionPane.showMessageDialog(this, "CAN NOT Print Empty Table!");
            }
            else{
                MessageFormat header=new MessageFormat("SALARY SHEET");
                MessageFormat footer=new MessageFormat("Page{0,number,integer}");
                try{
                    jTable1.print(JTable.PrintMode.NORMAL,header,footer);
                }catch(java.awt.print.PrinterException e){
                    JOptionPane.showMessageDialog(this, "Error Occured While Printing!");
                    System.err.format("Cannot Print %s%n", e.getMessage());
                }
            }
        
    }//GEN-LAST:event_jButton_printActionPerformed

